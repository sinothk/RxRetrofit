package com.sinothk.rxRetrofit.demo.bean;

public class UserEntity {
    private int id;
    private String userName;

    public UserEntity() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }
}
